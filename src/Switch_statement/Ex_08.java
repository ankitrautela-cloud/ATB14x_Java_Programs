package Switch_statement;

public class Ex_08 {
    public static void main(String[] args) {
        char code = 'C';
        switch (code) {
            default:
                System.out.println("Helloooooooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
